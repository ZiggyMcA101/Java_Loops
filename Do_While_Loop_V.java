package do_while_loop_v;
public class Do_While_Loop_V {

    public static void main(String[] args) {
    int rows = 8;

    for (int i = rows; i >= 1; --i) {
      for (int j = 1; j <= i; ++j) {
        System.out.print("$");
      }
      System.out.println();
    }
        }
    }
    

