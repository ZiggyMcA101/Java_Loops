package do_while_loop_iii;
public class Do_While_Loop_III {

    public static void main(String[] args) {
         int i = 1, n = 99;


    do  {
        
        i++;
     if (i%3 > 0)
      System.out.println(i);
      
     else if (i%3 <= 0);{
        System.out.println();
 
    }
    } while (i <= n);
    }    
    }
